export const getUnlockedLevels = () => {
  const progress = localStorage.getItem('aiGameProgress');
  if (!progress) return [1];
  
  const parsedProgress = JSON.parse(progress);
  const unlockedLevels = [1];
  
  // Level 2 unlocks if Level 1 passed with 80%+
  if (parsedProgress[1] && parsedProgress[1].percentage >= 80) {
    unlockedLevels.push(2);
  }
  
  // Level 3 unlocks if Level 2 passed with 80%+
  if (parsedProgress[2] && parsedProgress[2].percentage >= 80) {
    unlockedLevels.push(3);
  }
  
  return unlockedLevels;
};

export const saveGameProgress = (level, score, totalQuestions) => {
  const percentage = Math.round((score / totalQuestions) * 100);
  const progress = localStorage.getItem('aiGameProgress');
  let parsedProgress = {};
  
  if (progress) {
    parsedProgress = JSON.parse(progress);
  }
  
  parsedProgress[level] = {
    score,
    totalQuestions,
    percentage,
    completed: true,
    timestamp: new Date().toISOString()
  };
  
  localStorage.setItem('aiGameProgress', JSON.stringify(parsedProgress));
  return percentage;
};
